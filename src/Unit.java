public abstract class Unit 
	implements TurnBasedUnit{
	protected Square square;
	private int row;
	private int col;
	
	public Square getSquare(){
		return square;
	}
	
	public int getRow(){
		return row;
	}
	
	public int getCol(){
		return col;
	}
	
	public void setSquare(Square square){
		this.square = square;
	}
}
